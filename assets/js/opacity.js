        const clock = document.getElementById('MyClockDisplay');

        // Функция для получения текущего времени в формате "HH:MM"
        function getCurrentTime() {
            const now = new Date();
            const hours = String(now.getHours()).padStart(2, '0');
            const minutes = String(now.getMinutes()).padStart(2, '0');
            return `${hours}:${minutes}`;
        }

        // Функция для поиска ближайшего времени из массива, которое меньше или равно текущему
        function findClosestTime() {
            const currentTime = getCurrentTime();
            let closestTime = null;

            for (let i = times.length - 1; i >= 0; i--) {
                if (times[i].time <= currentTime) {
                    closestTime = times[i];
                    break;
                }
            }

            return closestTime || times[0]; // Если ничего не найдено, возвращаем первое время
        }

        // Функция для обновления времени на странице
        function updateClock() {
            const currentTime = getCurrentTime();
            const closestTime = findClosestTime();

            // Если текущее время совпадает с одним из времен в массиве
            if (closestTime.time === currentTime) {
                // Плавно исчезаем
                clock.style.opacity = 0;
                setTimeout(() => {
                    // Обновляем текст и плавно появляемся
                    clock.textContent = closestTime.text;
                    clock.style.opacity = 1;
                }, 2100); // Задержка для анимации исчезновения
            } else {
                // Иначе просто отображаем текст без анимации
                clock.textContent = closestTime.text;
                clock.style.opacity = 1;
            }
        }

        // Обновляем часы каждую секунду
        setInterval(updateClock, 60000);
        // Инициализация при загрузке страницы
        updateClock();


